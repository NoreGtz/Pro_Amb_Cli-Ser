    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        // TODO add your handling code here:
        int op = ComboBox_Opc.getSelectedIndex();
        int a = Integer.parseInt(txt_nA1.getText());
        int b = Integer.parseInt(txt_nA2.getText());
        int c = Integer.parseInt(txt_nB1.getText());
        int d = Integer.parseInt(txt_nB2.getText());
        
        if(a == 0 || b == 0 || c == 0 || d == 0)   
        {
            JOptionPane.showMessageDialog(this, "No puede ingresar valores de 0");
        }
        else         
        {
            try
            {
                Registry registro = LocateRegistry.getRegistry("127.0.0.1", 9999);
                iRMI interfaz = (iRMI)registro.lookup("mates");
            
                if(op == 0)
                {
                    JOptionPane.showMessageDialog(this,"Seleccione una operaci贸n valida");
                }
                if(op == 1)
                {    
                    signo.setText(" + ");
                    String suma;
                    suma = interfaz.sumar(a,b,c,d);
                    txt_res.setText(suma);   
                }
                if(op == 2)
                {
                    signo.setText(" - ");
                    String resta;
                    resta = interfaz.restar(a,b,c,d);
                    txt_res.setText(resta);
                }
                if(op == 3)
                {
                    signo.setText(" x ");
                    String multi;
                    multi = interfaz.multiplicar(a,b,c,d);
                    txt_res.setText(multi);
                }
                if(op == 4)
                {
                    signo.setText(" 梅 ");
                    String div;
                    div = interfaz.dividir(a,b,c,d);
                    txt_res.setText(div);
                }
            }
            catch(RemoteException | NotBoundException | NumberFormatException rex)
            {
                JOptionPane.showMessageDialog(this, rex.getMessage());
            }
        }    
    }//GEN-LAST:event_btn_calcularActionPerformed

